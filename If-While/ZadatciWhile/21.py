#21. Faktorijal nekog broja n je napisan kao n! i to je jednako 1*2*3...*(n-1)*n.
#Primjer: 
#4! = 1*2*3*4 = 24
#3! = 3*2*1 = 6
#2! = 2*1 = 2
#1! = 1
#0! = 1


broj=int(input("Unesite broj: "))
umnozak=1
while(broj>1):
    umnozak*=broj
    broj-=1
    print(umnozak)
    
    
    
    
    
    
    
    
    

    

















