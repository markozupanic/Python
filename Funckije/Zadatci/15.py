#15. napisi funkciju koja za primljenu listu vraca listu sa kvadratnim vrijednostima unesene liste.

def kvadriranje_liste():
    lista_kvadrata=[]
    brojevi=[1,2,3,4,5]
    for i in brojevi:
        print(i)
        umnozak=i**2
        lista_kvadrata.append(umnozak)
    print(lista_kvadrata)    






kvadriranje_liste()
























